<?php
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "testw3";

echo "<h2>Select data from DB</h2>";
// Create connection
$conn = new mysqli($servername, $username, $password, $dbname);
// Check connection
if ($conn->connect_error) {
  die("Connection failed: " . $conn->connect_error);
}

$sql = "SELECT id, firstname, lastname FROM MyGuests";
$result = $conn->query($sql);

if ($result->num_rows > 0) {
  // output data of each row
  while($row = $result->fetch_assoc()) {
    echo "id: " . $row["id"]. " - Name: " . $row["firstname"]. " " . $row["lastname"]. "<br>";
  }
} else {
  echo "0 results";
}

//######################################################################################################
echo "<h2>WHERE clause</h2><br>Where lastname = 'Doe'<br>"; 
$sql = "SELECT id, firstname, lastname FROM MyGuests WHERE lastname='Doe'"; //specificy wanted charateristic
$result = mysqli_query($conn, $sql);

if (mysqli_num_rows($result) > 0) {
  // output data of each row
  while($row = mysqli_fetch_assoc($result)) {
    echo "id: " . $row["id"]. " - Name: " . $row["firstname"]. " " . $row["lastname"]. "<br>";
  }
} else {
  echo "0 results";
}

//######################################################################################################
echo "<h2>ORDER BY clause</h2><br>Order By lastname<br>"; 
$sql = "SELECT id, firstname, lastname FROM MyGuests ORDER BY lastname DESC"; //specify order by
$result = mysqli_query($conn, $sql);

if (mysqli_num_rows($result) > 0) {
  // output data of each row
  while($row = mysqli_fetch_assoc($result)) {
    echo "id: " . $row["id"]. " - Name: " . $row["firstname"]. " " . $row["lastname"]. "<br>";
  }
} else {
  echo "0 results";
}
?>  